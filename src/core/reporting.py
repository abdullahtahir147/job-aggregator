def write_shortlist_md(
    *,
    jobs: list[Job],
    output_path: str | Path,
    run_id: str,
    generated_at: str,
    limit: int,
    uk_only: bool,
    exclude_seniority: list[str],
    intent_enabled: bool = False,
) -> None:
    """Write a Markdown shortlist of the top *limit* jobs."""
    output_path = Path(output_path)
    output_path.parent.mkdir(parents=True, exist_ok=True)

    lines: list[str] = []
    lines.append("# Shortlist")
    lines.append("")
    lines.append(f"**Run ID:** `{run_id}`")
    lines.append(f"**Generated:** {generated_at}")
    lines.append(f"**Top N:** {limit}")
    lines.append(f"**UK only:** {uk_only}")
    if exclude_seniority:
        lines.append(f"**Excluded seniority:** {', '.join(exclude_seniority)}")
    lines.append(f"**Intent scoring:** {'enabled' if intent_enabled else 'off'}")
    lines.append(f"**Results:** {len(jobs)}")
    lines.append("")

    if not jobs:
        lines.append("_No jobs matched the shortlist criteria._")
    else:
        # Group by tag in priority order
        from src.core.taxonomy import TAG_PRIORITY, DEFAULT_TAG
        tag_order = TAG_PRIORITY + [DEFAULT_TAG]
        grouped: dict[str, list[tuple[int, Job]]] = {}
        for i, job in enumerate(jobs, 1):
            grouped.setdefault(job.tag, []).append((i, job))

        for tag in tag_order:
            items = grouped.get(tag)
            if not items:
                continue
            lines.append(f"### {tag} ({len(items)})")
            lines.append("")
            if intent_enabled:
                lines.append("| # | Company | Title | Location | Age | Score | Base | Δ | Link | Reason |")
                lines.append("|---|---------|-------|----------|-----|-------|------|---|------|--------|")
                for rank, job in items:
                    link = f"[Apply]({job.url})" if job.url else "—"
                    reason = _short_reason(job.score_reasons)
                    if job.recency_reason:
                        reason += f"; {job.recency_reason}"
                    d = job.intent_delta + job.recency_delta
                    d_str = f"+{d}" if d > 0 else str(d) if d else "0"
                    age_str = f"{job.age_days}d" if job.age_days is not None else "-"
                    lines.append(
                        f"| {rank} | {job.company} | {job.title} | {job.location}"
                        f" | {age_str} | {job.final_score} | {job.score} | {d_str} | {link} | {reason} |"
                    )
            else:
                lines.append("| # | Company | Title | Location | Age | Score | Link | Reason |")
                lines.append("|---|---------|-------|----------|-----|-------|------|--------|")
                for rank, job in items:
                    link = f"[Apply]({job.url})" if job.url else "—"
                    reason = _short_reason(job.score_reasons)
                    if job.recency_reason:
                        reason += f"; {job.recency_reason}"
                    age_str = f"{job.age_days}d" if job.age_days is not None else "-"
                    lines.append(
                        f"| {rank} | {job.company} | {job.title} | {job.location}"
                        f" | {age_str} | {job.score} | {link} | {reason} |"
                    )
            lines.append("")

    lines.append("---")
    lines.append("_Generated by job-aggregator shortlist_")

    output_path.write_text("\n".join(lines), encoding="utf-8")
    logger.info("Shortlist MD written to %s (%d jobs)", output_path, len(jobs))


def write_shortlist_csv(
    *,
    jobs: list[Job],
    output_path: str | Path,
    intent_enabled: bool = False,
) -> None:
    """Write a CSV shortlist for easy filtering."""
    output_path = Path(output_path)
    output_path.parent.mkdir(parents=True, exist_ok=True)

    if intent_enabled:
        fieldnames = [
            "rank", "tag", "company", "title", "location", "age_days",
            "final_score", "base_score", "intent_delta", "recency_delta",
            "url", "match_reason", "intent_reasons", "recency_reason",
        ]
    else:
        fieldnames = [
            "rank", "tag", "company", "title", "location", "age_days",
            "score", "url", "match_reason", "recency_reason",
        ]

    with open(output_path, "w", newline="", encoding="utf-8") as fh:
        writer = csv.DictWriter(fh, fieldnames=fieldnames)
        writer.writeheader()
        for i, job in enumerate(jobs, 1):
            age = job.age_days if job.age_days is not None else ""
            if intent_enabled:
                writer.writerow({
                    "rank": i,
                    "tag": job.tag,
                    "company": job.company,
                    "title": job.title,
                    "location": job.location,
                    "age_days": age,
                    "final_score": job.final_score,
                    "base_score": job.score,
                    "intent_delta": job.intent_delta,
                    "recency_delta": job.recency_delta,
                    "url": job.url or "",
                    "match_reason": "; ".join(job.score_reasons) if job.score_reasons else "",
                    "intent_reasons": "; ".join(job.intent_reasons) if job.intent_reasons else "",
                    "recency_reason": job.recency_reason,
                })
            else:
                writer.writerow({
                    "rank": i,
                    "tag": job.tag,
                    "company": job.company,
                    "title": job.title,
                    "location": job.location,
                    "age_days": age,
                    "score": job.score,
                    "url": job.url or "",
                    "match_reason": "; ".join(job.score_reasons) if job.score_reasons else "",
                    "recency_reason": job.recency_reason,
                })

    logger.info("Shortlist CSV written to %s (%d jobs)", output_path, len(jobs))


