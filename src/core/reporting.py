def write_brief_md(
    *,
    output_path: str | Path,
    run: RunRecord,
    shortlist_jobs: list[Job],
    intent_enabled: bool = False,
    new_uk_count: int = 0,
    unknown_ats_count: int = 0,
) -> None:
    """Write a human-readable brief with compact bullet-style job listings."""
    output_path = Path(output_path)
    output_path.parent.mkdir(parents=True, exist_ok=True)

    lines: list[str] = []
    lines.append("# Job Brief")
    lines.append("")
    lines.append(f"**Run:** `{run.run_id}` | {run.run_time}")
    lines.append(f"**Companies:** {run.companies_processed} | "
                 f"**Jobs:** {run.jobs_found} | "
                 f"**New:** {run.jobs_new} | "
                 f"**New UK:** {new_uk_count} | "
                 f"**Unknown ATS:** {unknown_ats_count}")
    lines.append("")

    apply_now = shortlist_jobs[:8]
    worth_a_look = shortlist_jobs[8:20]

    def _job_bullet(job: Job) -> list[str]:
        tag_label = f" [{job.tag}]" if job.tag != "OTHER" else ""
        age_str = f" | {job.age_days}d old" if job.age_days is not None else ""
        if intent_enabled:
            d = job.intent_delta + job.recency_delta
            d_str = f"+{d}" if d > 0 else str(d) if d else "0"
            score_str = f"{job.final_score} (base {job.score}, Δ{d_str})"
        else:
            score_str = str(job.score)
        reason = _short_reason(job.score_reasons)
        intent_r = ""
        if intent_enabled and job.intent_reasons:
            intent_r = " | " + _truncate("; ".join(job.intent_reasons), 80)
        recency_r = ""
        if job.recency_reason:
            recency_r = f" | {job.recency_reason}"
        bullet: list[str] = []
        bullet.append(f"- **{job.company} — {job.title}**{tag_label}  Score: {score_str}{age_str}")
        bullet.append(f"  {job.location}")
        bullet.append(f"  Why: {reason}{intent_r}{recency_r}")
        if job.url:
            bullet.append(f"  [Apply]({job.url})")
        return bullet

    if apply_now:
        lines.append("## Apply Now")
        lines.append("")
        for job in apply_now:
            lines.extend(_job_bullet(job))
            lines.append("")

    if worth_a_look:
        lines.append("## Worth a Look")
        lines.append("")
        for job in worth_a_look:
            lines.extend(_job_bullet(job))
            lines.append("")

    if not apply_now and not worth_a_look:
        lines.append("_No shortlisted jobs to show._")
        lines.append("")

    lines.append("---")
    lines.append("_Generated by job-aggregator brief_")

    output_path.write_text("\n".join(lines), encoding="utf-8")
    logger.info("Brief written to %s (%d jobs)", output_path, len(shortlist_jobs))


